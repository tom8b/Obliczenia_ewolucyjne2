using System;
using System.Linq;

namespace ConsoleApp1
{
    public class Inwersja
    {
        public Individual Inwersuj(Individual individual, int a, int b)
        {
            var lewaGranica = WybierzPunktKrzyzowania(0, individual.NumberOfBits - 2);
            var prawaGranica = WybierzPunktKrzyzowania(lewaGranica, individual.NumberOfBits);
            Array.Reverse(individual.X1Binary, lewaGranica, prawaGranica - lewaGranica);
            Array.Reverse(individual.X2Binary, lewaGranica, prawaGranica - lewaGranica);
            return individual;
        }

        private int WybierzPunktKrzyzowania(int min, int max)
        {
            Random random = new Random();
            return random.Next(min, max);
        }
    }
}
